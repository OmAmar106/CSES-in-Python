class DSU:
    def __init__(self,n):
        self.n = n
        self.parent = [i for i in range(n)]
        self.set_size = [1]*n
    def find(self,a):
        if self.parent[a]==a:return a
        t = self.find(self.parent[a])
        self.parent[a] = t
        return t
    def union(self,a,b):
        a = self.find(a);b = self.find(b)
        if a!=b:
            if self.set_size[a]<self.set_size[b]:
                a,b = b,a
            self.parent[b] = a
            self.set_size[a] += self.set_size[b]
            self.set_size[b] = 0

