class BIT:
    def __init__(self, x):
        self.bit = x
        self.bit1 = x[:]
    def update(self, idx, x,idx1):
        while idx < len(self.bit):
            self.bit[idx] += x
            self.bit1[idx] += x*idx1
            idx |= idx + 1
    def query(self, end):
        x = 0
        x1 = 0
        while end:
            x += self.bit[end - 1]
            x1 += self.bit1[end - 1]
            end &= end - 1
        return x,x1

